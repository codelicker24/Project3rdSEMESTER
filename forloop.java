public class forloop {
    public static void main(String[] args){
        int i;
        for(i=0;i<=10;i++)
        {
            if(i%2==0){
                System.out.println("this is an even number"+i);

            }
            else{
                 System.out.println("this is an odd number"+i);
            }
        }
    }
}
